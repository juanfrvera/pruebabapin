using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Contract.Base;
namespace Contract
{
    [Serializable]
    public class ProyectoLocalizacionFilter : _ProyectoLocalizacionFilter
    {
        private List<int> idsProyecto;
        public List<int> IdsProyecto
        {
            get
            {
                if (idsProyecto == null) idsProyecto = new List<int>();
                return idsProyecto;
            }
            set { idsProyecto = value; }
        }
    }
